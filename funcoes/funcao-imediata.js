//é uma função que é chamada(executada) assim que é criada

(function(num1, num2) {
    console.log(num1 + num2)
})(1,3)

/**
 * 
(function() { ... }) → cria uma função anônima (sem nome).
Os parênteses externos () transformam ela em uma expressão (Expression).
O segundo par de parênteses () no final executa a função na hora.
 */