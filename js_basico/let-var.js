/**
    let 
        - tem escopo de bloco {...bloco} (ele pode procurar no bloco superior)
        - Visível apenas dentro do bloco onde foi declarada ({ ... }).
        - Não pode ser redeclarada no mesmo escopo.

    var 
        - tem escopo de função
        - Visível dentro da função onde foi declarada.
        - Ignora blocos como if, for, while, etc.
        - Pode ser redeclarada dentro do mesmo escopo.
 */